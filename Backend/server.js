// --- POINT DANGER API ---
app.get("/danger", (req, res) => {
  const lat = parseFloat(req.query.lat);
  const lon = parseFloat(req.query.lon);
  const point = ee.Geometry.Point([lon, lat]).buffer(100);

  const dem = ee.Image("USGS/SRTMGL1_003");
  const slope = ee.Terrain.slope(dem);
  const rainfall = ee.ImageCollection("NASA/GPM_L3/IMERG_V06")
    .filterDate("2023-08-01", "2023-08-31")
    .select("precipitationCal")
    .mean();

  const stack = ee.Image.cat([slope.rename("slope"), rainfall.rename("rainfall")]);

  const stats = stack.reduceRegion({
    reducer: ee.Reducer.mean(), geometry: point, scale: 30, maxPixels: 1e9
  });

  stats.evaluate((data, err) => {
    if (err) { res.status(500).send(err); return; }

    const s = data.slope || 0;
    const r = data.rainfall || 0;

    let cliffRisk = 10;
    if (s > 45) cliffRisk = 90;
    else if (s > 35) cliffRisk = 70;
    else if (s > 25) cliffRisk = 50;
    else if (s > 15) cliffRisk = 30;

    let rainRisk = 10;
    if (r > 15) rainRisk = 80;
    else if (r > 10) rainRisk = 60;
    else if (r > 5) rainRisk = 40;

    const riskScore = Math.round(cliffRisk * 0.7 + rainRisk * 0.3);
    let status = "SAFE";
    if (riskScore > 70) status = "EXTREME DANGER";
    else if (riskScore > 50) status = "HIGH RISK";
    else if (riskScore > 30) status = "MODERATE RISK";

    res.json({
      slope: s.toFixed(1),
      rainfall: r.toFixed(2),
      cliffRisk,
      rainRisk,
      riskScore,
      status
    });
  });
});

