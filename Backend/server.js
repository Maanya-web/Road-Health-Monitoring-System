// --- SLOPE API ---
app.get("/slope", (req, res) => {
  const lat = parseFloat(req.query.lat);
  const lon = parseFloat(req.query.lon);
  const point = ee.Geometry.Point([lon, lat]);

  const dem = ee.Image("USGS/SRTMGL1_003");
  const slope = ee.Terrain.slope(dem);

  const result = slope.reduceRegion({
    reducer: ee.Reducer.mean(),
    geometry: point,
    scale: 30
  });

  result.evaluate((data, err) => {
    if (err) { res.status(500).json({ error: err }); return; }
    res.json({ slope: data.slope });
  });
});

// --- SNOW API ---
app.get("/snow", (req, res) => {
  const lat = parseFloat(req.query.lat);
  const lon = parseFloat(req.query.lon);

  const elevation = 2000 + Math.random() * 1500;
  const slope = Math.random() * 40;
  const aspect = Math.random() * 360;
  let risk = "LOW";

  if (elevation > 2500 && (aspect > 315 || aspect <= 45)) risk = "CRITICAL ICE RISK";
  else if (slope > 30) risk = "AVALANCHE RISK";
  else if (elevation > 2200) risk = "MODERATE SNOW RISK";

  res.json({
    name: "Highway near location",
    status: elevation > 2300 ? "Snow Covered" : "Clear",
    elevation: elevation.toFixed(0),
    slope: slope.toFixed(1),
    aspect: aspect.toFixed(0),
    risk: risk
  });
});

// --- FLOOD API ---
app.get("/flood", (req, res) => {
  const lat = parseFloat(req.query.lat);
  const lon = parseFloat(req.query.lon);
  const date = req.query.date;
  const point = ee.Geometry.Point([lon, lat]).buffer(60);

  const dem = ee.Image("USGS/SRTMGL1_003");
  const slope = ee.Terrain.slope(dem);
  const water = ee.Image("JRC/GSW1_4/GlobalSurfaceWater").select("occurrence");
  const rivers = water.gt(50);
  const riverDist = rivers.not().fastDistanceTransform(256).multiply(30).rename("riverDist");

  const s1 = ee.ImageCollection("COPERNICUS/S1_GRD")
    .filterBounds(point)
    .filter(ee.Filter.listContains("transmitterReceiverPolarisation", "VV"))
    .filter(ee.Filter.eq("instrumentMode", "IW"))
    .select("VV");

  const dateEE = ee.Date(date);
  const before = s1.filterDate(dateEE.advance(-45, "day"), dateEE.advance(-15, "day")).median();
  const after = s1.filterDate(dateEE.advance(-5, "day"), dateEE.advance(5, "day")).median();

  const beforeLin = ee.Image(10).pow(before.divide(10));
  const afterLin = ee.Image(10).pow(after.divide(10));
  const ratio = beforeLin.divide(afterLin);
  const flood = ratio.gt(1.5).rename("flood");

  const stack = ee.Image.cat([flood, slope.rename("slope"), riverDist]);

  const stats = stack.reduceRegion({
    reducer: ee.Reducer.mean(), geometry: point, scale: 30, maxPixels: 1e9
  });

  stats.evaluate((data, err) => {
    if (err) { res.status(500).json({ error: err }); return; }

    const floodFrac = data.flood || 0;
    const slopeVal = data.slope || 0;
    const distVal = data.riverDist || 0;

    let status = "Clear";
    if (floodFrac > 0 && floodFrac <= 0.05) status = "Water Present";
    else if (floodFrac > 0.05 && floodFrac <= 0.20) status = "Excess Water";
    else if (floodFrac > 0.20) status = "Flooded";

    let risk = "LOW";
    if (slopeVal < 10 && distVal < 150) risk = "CRITICAL";
    else if (slopeVal < 15 && distVal < 400) risk = "MODERATE";

    res.json({
      name: "Highway near location",
      status: status,
      floodFrac: (floodFrac * 100).toFixed(1),
      slope: slopeVal.toFixed(1),
      riverDist: distVal.toFixed(0),
      risk: risk
    });
  });
});

